import nltk
nltk.download('punkt')
from nltk.tokenize import word_tokenize
def nltk_tokenizer(text):
    tokens = word_tokenize(text)
    return tokens

text = "Hello, world! This is an NLTK tokenization example."
tokens = nltk_tokenizer(text)
print(tokens)

